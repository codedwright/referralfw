                                <p>Since the age of 12, I have been an entrepreneur with lots of energy. I had many lawn mowing jobs, both residential and commercial. Working as a bus boy at Denny’s gave me the inside edge to persuade my manager to use my lawn service instead of the company he was using that came all the way from Indianapolis. This company did all of the Denny’s Indianapolis locations and also the Fort Wayne locations once a week. They also cleaned the windows on the same trip. They no longer wanted to make the trip just to clean the windows, so my manager asked me if I would like to learn how to clean windows. I said, “Sure!” and went to Janitor’s Supply and bought my window cleaning equipment supplies. Soon, I had several other commercial window cleaning accounts. I was informed that the company from Indianapolis no longer wanted to do the monthly carpet cleaning service for the Fort Wayne Denny’s locations since they did not have the entire package of services any longer. My manager asked me if I would like to learn how to clean the restaurant’s carpet. I said, “Sure!” and rented a Rug Doctor at the Golden Dome hardware store (now out of business). I chose Golden Dome hardware because they closed at 1:00 PM on Saturday and did not charge for Sunday. I had to have the machine back at 8:00 AM Monday morning. I would always line up another carpet cleaning job to help offset the cost of the rental.</p>
                                <p>At this time, my business was called “R &amp; F Enterprises” and my services included lawn care, window washing, and carpet cleaning. I maintained good grades in high school, so I was awarded a four-year Hoosier scholarship, which would pay for tuition at any college in Indiana. I attended college for one year as an accounting major, with the goal of becoming a Certified Public Accountant. I decided I would rather work with my hands instead of sit behind a desk. I then attended Ivy Tech for electronics, but this field did not interest me either. At this time, I made the decision to pursue my carpet cleaning business. I bought a commercial-duty Rug Doctor to use for cleaning.</p>
                                <p>I knew my business name had to change since I was phasing out the lawn care and window cleaning divisions. I tried the name C.C.C. or “Complete Carpet Cleaning” for a short time, but I felt it was just too generic. I realized I was getting all of my business from referrals, so I decided to name my business “Referral Carpet Cleaning.”</p>
                                <p>In 1980, I married Anita, who worked in the accounting department at a local credit union. We put our first Yellow Page display ad in the phone book in 1981. As business picked up, Anita left her job at the credit union and began caring for the accounting, scheduling and answering the phones at Referral. In the fall of 1982, I upgraded and purchased my first electric truck mounted equipment and decided to hire my friend and first employee, Eric. In 1988, I contracted our current office location at 2901 Parnell Avenue. The building was built to house three other tenants to help with the cost of the building, maintenance, and taxes.</p>
                                <p>Throughout the years, our business has been built upon honesty and quality workmanship. In the summer of 1999, we upgraded our electric truck mount to a PTO Butler Truck Mount powered by the van’s V-8 Engine. This produces hotter water and stronger vacuum which allows us to get our customers’ carpet extra clean and more importantly, dry quickly! We were so happy with it, we purchased a second Butler Truck Mount in the summer of 2000. In 2003, we purchased a box truck with a Butler Truck Mount. We customized the interior of the box to hold drying equipment and tools for emergency water damage restoration. This allowed us to respond quicker to water damage emergencies and to have all the tools on board. In 2004, we began cleaning tile & grout floors. In 2013, we began cleaning hardwood floors. In 2015, we purchased a brand new, larger box truck to help us more efficiently restore homes and businesses damaged by water.</p>
                                <p>My sons, Alan and Ryan, were born in 1984 and 1986, respectively. I always hoped my sons would continue the business I started. From a young age, they would come with me on some commercial restaurant jobs and they would get paid to move the tables and remove the chewing gum off of the carpet.</p>
                                <p>In 2000, at the age of 16, Alan worked full time during the summer and part-time after school and on the weekends. After graduating from Carroll High School in 2002, Alan attended IPFW and received an Associates Degree in Organizational Leadership and Supervision. Alan is the General Manager at Referral and he continues to ensure Referral’s high standards are upheld. Alan married his wife, Audrey, in 2014 and she joined Referral as the director of marketing and business development toward the end of 2015.</p>
                                <p>Ryan worked full time at Referral after graduating from Carroll High School in 2005. He married his wife, Abby, in 2013 and they are currently full-time volunteers in a Bible education work.</p>
                                <p>I want my clients to receive the top quality service they expect from Referral.  I have toured many cleaning and restoration businesses around the country and found some of them to be well run, organized and very skilled.  However, some “cleaning companies” have a shop and equipment that is disorganized, dirty or broken.  Some of their technicians receive a quick 2-4 week training and then are paid commission and put in charge of a truck.</p>
                                <p>My goal is to remain a small business so that we can maintain the trust and personal relationship we have with our clients, but also our high standard of quality by thoroughly training and teaching our technicians all of the details we have learned over the years.</p>
